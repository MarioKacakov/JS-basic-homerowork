// Description Celsius to Fahrenheit formula X celsius x1.8 + 32

function celsiusToFahrenheit(celsius) {
    let convertCelsius = (celsius * 1.8) + 32;
    console.log(convertCelsius) ;
}

celsiusToFahrenheit(20);

function fahrenheitToCelsius(fahrenheit) {
    let convertFahrenheit = (5 / 9) * (fahrenheit - 32);
    console.log(convertFahrenheit);
}

fahrenheitToCelsius(68);




